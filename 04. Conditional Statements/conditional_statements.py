#conditional statements check if a condition is true.
age = 21
name = 'jessy'
#Equality
print(age == 21)
#Inequality
print(age != 21)
#Less than
print(age < 21)
#Less than or equal to
print(age <= 21)
#Greater than
print(age > 21)
#Greater than or equal to
print(age >= 21)
#String equality
print(name == 'jessy')
#String Inequality
print(name != 'jessy')
#You can also check whether or not an item is in a list.
my_state = 'WA'
northwest_states = ['WA', 'MA']
print(my_state in northwest_states)
print(my_state not in northwest_states)
