#Round a float to the given number of places.
print(round(3.1415926, 2))

#Get multiples of ten.
print(round(234, -2))
print(round(2234, -3))

#Get the absolute values.
print(abs(-5))

#Convert base-10 numbers to other bases.
print(bin(10))
print(oct(20))
print(hex(20))

#Represent a complex number.
print(complex(2, 3))


