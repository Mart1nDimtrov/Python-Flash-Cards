#A string is a value made up of one or more characters,
#surrounded by single or double quotes.
message = 'I love Python!'
print(message)

#You can use quotation marks inside strings.
python_quote = 'I said, "I love Python!"'
print(python_quote)

#You can also insert tabs and newlines.
message = 'Grocery list:\n\tmilk\n\teggs'
print(message)